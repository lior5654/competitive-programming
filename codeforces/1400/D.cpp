const int maxn = 3e3 + 5;

int a[maxn]; int n;
void solve() {
    cin >> n;
    for(int i = 1; i <= n; ++i) {
        cin >> a[i];
    }
    ll res = 0;
    vector<int> lcnt(n+1); vector<int> rcnt(n+1);
    for(int i = 1; i <= n-3; ++i) {
        ll cres = 0;
        for(int j = 1; j <= n; ++j) {
            lcnt[j] = 0; rcnt[j] = 0;
        }
        lcnt[a[i+1]]++;
        for(int j = i+3; j <= n; ++j) {
            rcnt[a[j]]++;
        }
        cres = rcnt[a[i+1]];
        if(a[i] == a[i+2])
            res += cres;
        for(int k = i+3; k < n; ++k) {
            // getting a[k-1] to l, removing a[k] from r
            cres += rcnt[a[k-1]];
            lcnt[a[k-1]]++;
            cres -= lcnt[a[k]];
            rcnt[a[k]]--;
            if(a[i] == a[k])
                res += cres;
        }

    }
    cout << res << '\n';
}























































 
