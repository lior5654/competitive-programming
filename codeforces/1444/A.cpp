vpl primeFactors(ll k)  
{  
    // Print the number of 2s that divide n  
    vpl out;
    ll meme = 1;
    while (k % 2ll == 0)  
    {  
        meme *= 2ll;
        k = k/2ll;  
    }  
    if(meme > 1)
    {
        out.emplace_back(2ll, meme);
    }
  
    // n must be odd at this point. So we can skip  
    // one element (Note i = i +2)  
    for (ll i = 3; i <= sqrt(k); i = i + 2ll)  
    {  
        // While i divides n, print i and divide n  
        meme = 1;
        while (k % i == 0ll)  
        {  
            meme *= i;
            k = k/i;  
        }  
        if(meme > 1)
        {
            out.emplace_back(i, meme);
        }
    }  
  
    // This condition is to handle the case when n  
    // is a prime number greater than 2  
    if (k > 2ll)  
        out.pb({k, k});
    return out;
}  
  

void solve()
{
    ll p, q; cin >> p >> q;
    if(p % q)
    {
        cout << p << '\n'; return;
    }
    vpl primes = primeFactors(q);
    ll bres = INFL;
    int sz = primes.size();
    for(int i = 0; i < sz; ++i)
    {
        ll tmp = p;
        ll res = 1;
        while(!(tmp%primes[i].fi) && tmp >= primes[i].se && tmp >= q && !(tmp%q))
        {
            tmp /= primes[i].fi;
            res *= primes[i].fi;
        }
        if(res < bres)
        {
            bres = res;
        }
    }

    cout << p / bres  << '\n'; return;
}










































