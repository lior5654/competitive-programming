const int maxm = 11;
const int maxn = 1e3 + 5;
const ll mod =  1e9 + 7;
ll dp[maxm][maxn];
void solve()
{
    int n, m; cin >> n >> m;
    for(int i = 1; i <= n; ++i)
    {
        dp[1][i] = 1;
    }
    for(int i = 2; i <= m; ++i)
    {
        for(int j = 1; j <= n; ++j)
        {
            for(int k = 1; k <= j; ++k)
            {
                dp[i][j] += dp[i-1][k];
                dp[i][j] %= mod;
            }
            
        }
    }
    ll res = 0;
    for(int i = 1; i <= n; ++i)
    {
        for(int j = 1; j <= i; ++j)
        {
            res += dp[m][n - i + 1] * dp[m][j];
            res %= mod;
        }
    }
    cout << res << '\n';    

}




























