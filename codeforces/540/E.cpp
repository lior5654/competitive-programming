void solve()
{

    int n; cin >> n;
    int cnt = 0;
    ordered_set bruh;
    rep(i, n)
    {
        int a, b; cin >> a >> b;
        if(!ev.count(a))
        {
            ev[a] = a;
            bruh.insert(a);
            cnt++;
        }
        if(!ev.count(b))
        {
            ev[b] = b;
            cnt++;
            bruh.insert(b);
        }
        int temp = ev[a];
        ev[a] = ev[b];
        ev[b] = temp;
    }

    ll res = 0;
    vector<ll> ar;
    for(auto e : ev)
    {
        cnt--;
        ar.pb(e.se);
        if(e.fi == e.se) continue;
        if(e.fi >= e.se)
        {
            res += e.fi - 1 - (e.se + 1) + 1 - (bruh.order_of_key(e.fi) - bruh.order_of_key(e.se+1));
            continue;
        }
        res += e.se - 1 - (e.fi + 1) + 1 - (bruh.order_of_key(e.se) - bruh.order_of_key(e.fi+1));
    }
    ordered_set bruh2;
    int sz = ar.size();
    for(int i = sz - 1; i >= 0; --i)
    {
        res += bruh2.order_of_key(ar[i]);
        bruh2.insert(ar[i]);
    }
    cout << res << endl;



}




























