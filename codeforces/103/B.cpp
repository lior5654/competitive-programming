bool dfs(int c, int p = 0)
{
    vis[c] = true;
    for(auto e : g[c])
    {
        if(vis[e])
        {
            if(e!=p)
            {
                magic = {c, e};
                return true;
            }
        }
        else
        {
            if(dfs(e, c)) return true;
        }
        
    }
    return false;
}
bool dfs2(int c, int p = 0)
{
    cnt++;
    vis[c] = true;
    for(auto e : g[c])
    {
        if(magic != mp(e, c) && magic != mp(c, e) && e != p)
        {
            if(vis[e]) return false;
            dfs2(e, c);
        }
    }
    return true;
}
