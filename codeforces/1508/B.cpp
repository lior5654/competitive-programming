ll n, k;
const int UPPER = 61;
ll dp[100][100];
ll sdp[100];
void solve() {
    cin >> n >> k;
    int next = 1;
    vi res;
    while(n > UPPER) {
        res.pb(next);
        next++;
        n--;
    }
    vi left;
    rep(i, n) {
        left.pb(next+i);
    }
    ll mc = 0;
    int i = 0;
    while(i < n) {
        int j = 1;
        int nn = (n-i);
        while(j < nn && mc + dp[j][nn] < k) {
            mc += dp[j][nn];
            j++;
        }
        for(int e = j-1; e >= 0; --e) {
            res.pb(left[e]);
        }
        vi nleft;
        for(int e = j; e < left.size(); ++e) {
            nleft.pb(left[e]);
        }
        left=nleft;
        i += j;
    }
    if(mc != k-1) {
        cout << -1 << '\n'; return;
    }
    for(auto e : res) {
        cout << e << ' ';
    }
    cout << '\n';


}
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 void init() {
    dp[1][0] = 1;
    dp[1][1] = 1;
    sdp[0] = 1;
    sdp[1] = 1;
    for(int i = 2; i <= UPPER; ++i) {
        for(int j = 1; j <= i; ++j) {
            dp[j][i] = sdp[i-j];
            sdp[i] += dp[j][i];
        }
    }
 }
 
 
 
