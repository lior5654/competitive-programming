const int maxn = 1e5 + 5;
int a[maxn]; int b[maxn]; int c[maxn]; int n; int m;
int ans[maxn];
stack<int> g[maxn];
int vis[maxn] = {0};
#define BRUH_WHY_TESTCASES
void solve()
{
    cin >> n >> m;
    for(int i = 1; i <= n; ++i) {
        vis[i] = -1;
        g[i] = stack<int>();
    }
    rep(i, n) {
        cin >> a[i+1];
    }
    rep(i, n) {
        cin >> b[i+1];
        if(a[i+1] != b[i+1]) {
            g[b[i+1]].push(i+1);
        } 
        vis[b[i+1]] = i+1;
    }
    rep(i, m) {
        cin >> c[i+1];
    }

    for(int i = m; i >= 1; --i) {
        if(!g[c[i]].empty()) {
            int to = g[c[i]].top(); g[c[i]].pop();
            ans[i] = to;
        } else {
            if(i!= m) {
                ans[i] = ans[i+1];
            } else {
                if(vis[c[i]]>=0) {
                    ans[i] = vis[c[i]];
                } else {
                    cout << "NO" << '\n'; return;
                }
            }
        }
    }
    for(int i = 1; i <= n; ++i) {
        if(!g[i].empty()) {
            cout << "NO" << '\n'; return;
        }
    }
    cout << "YES" << '\n';
    for(int i = 1; i <= m; ++i) {
        cout << ans[i] << ' ';
    }
    cout << '\n';

}










































