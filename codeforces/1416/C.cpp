const int maxn = 3e5 +5;
int a[maxn];
int n;
int x = 0;
ll res = 0;

void solve()
{
    cin >> n;
    aread(a, n);
    vvi groups;
    vi s;
    rep(i, n)
    {
        s.pb(i);
    }
    groups.pb(s);
    for(int b = 31; b >= 0; --b)
    {
        array<ll, 2> inv = { 0 };
        for(auto g : groups)
        {
            array<ll, 2> cnt = { 0 };
            for(auto i : g)
            {
                cnt[(a[i]&(1<<b))>0]++;
                inv[(a[i]&(1<<b))>0] += cnt[!((a[i]&(1<<b))>0)];
            } 
        }
        res += min(inv[0], inv[1]);
        if(inv[0] > inv[1])
        {
            x |= 1<<b;
            rep(i, n)
            {
                a[i] ^= 1<<b;
            }
        }
        int sz = groups.size();
        vvi next;
        rep(i, sz)
        {
            array<vi, 2> split;
            for(auto e : groups[i])
            {
                split[(a[e]&(1<<b))>0].pb(e);
            }
            for(auto s : split)
            {
                if(!s.empty())
                {
                    next.pb(s);
                }
            }
        }
        groups = next;
    }
    cout << res << ' ' << x << '\n';
}
 

 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
