const int maxn = 1e5 + 5;

struct mission
{
    int x, y, t;
} mis[maxn];
int dp[maxn]; // dp[i] - the answer when I pick time i
void solve()
{
    memset(dp, 0xFF, sizeof(dp)); // set all values initally to -1
    int r, n; cin >> r >> n;
    rep(i, n)
    {
        cin >> mis[i+1].t >> mis[i+1].x >> mis[i+1].y;
    }
    mis[0].x = 1;
    mis[0].y = 1;
    mis[0].t = 0;
    dp[0] = 0;
    int next = 0;
    int mx = -1;
    int ans = 0;
    for(int i = 1; i <= n; ++i)
    {
        while(mis[i].t - mis[next].t >= 2 * r)
        {
            mx = max(mx, dp[next]);
            next++;
        }
        if(mx >= 0)
        {
            dp[i] = mx + 1;
        }
        for(int j = i - 1; j >= 0 && mis[i].t - mis[j].t < 2 * r; --j)
        {
            if(abs(mis[j].x - mis[i].x) + abs(mis[j].y - mis[i].y) <= mis[i].t - mis[j].t && dp[j] >= 0)
            {
                dp[i] = max(dp[i], 1 + dp[j]);
            }
        }
        ans = max(ans, dp[i]);
    }
    cout << ans << '\n';



}
 

 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
 
